class Lead {
  final String id;
  final String name;
  final String email;
  final String company;
  final String status;
  final String source;
  final double estimatedValue;

  Lead({
    required this.id,
    required this.name,
    required this.email,
    required this.company,
    required this.status,
    required this.source,
    required this.estimatedValue,
  });
}
